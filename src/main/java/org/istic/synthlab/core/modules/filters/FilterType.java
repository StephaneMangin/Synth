package org.istic.synthlab.core.modules.filters;

/**
 *  The enum Filter type.
 *
 * @author Stephane Mangin <stephane[dot]mangin[at]freesbee[dot]fr>
 *
 */
public enum FilterType {
    /**
     * Allpass filter type.
     */
    ALLPASS,
    /**
     * Highpass filter type.
     */
    HIGHPASS,
    /**
     * Lowpass filter type.
     */
    LOWPASS,
    /**
     * Statevariable filter type.
     */
    STATEVARIABLE
}